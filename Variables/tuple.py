#Tuple - collection of values and immutable
#you want a set of value and you don't want to change the values then we can use tuples
#Since you don't change the values iteration in tuples are faster
tup = (21,36,14,25)
print(tup)
#when you want to fetch values you need to use square bracket
print(tup[1])
#tuples are immutable and yuo can't change values
#tup[1] = 30
#count will count no of occurance in a tuple
print(tup.count(21))
#you can find the index of particular value
print(tup.index(25))
