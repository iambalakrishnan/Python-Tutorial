#complement of of 12 --> -13
#1' complement + 1
print(~12)
print(f"1' complement is : {~1}")
print(f"2' complement is : {~2}")
print(f"12' complement is : {~12}")
print(f"4' complement is : {~4}")

#Bitwise AND
print(f"bitwise AND of 12 & 13  : {12 & 13}")
print(f"bitwise AND of 25 & 30  : {25 & 30}")

#Bitwise OR
print(f"bitwise OR of 12 | 13  : {12 | 13}")
print(f"bitwise OR of 25 | 30  : {25 | 30}")

#Bitwise XOR
print(f"bitwise XOR of 12 ^ 13  : {12 ^ 13}")#1
print(f"bitwise XOR of 25 ^ 30  : {25 ^ 30}")#14

#Left shift
print(f"left shift of 10 << 2 is : {10<<2}") #40
print(f"left shift of 15 << 2 is : {15<<2}")#60

#Right shift
print(f"Right shift of 10 << 2 is : {10>>2}")
print(f"Right shift of 15 << 2 is : {15>>2}")