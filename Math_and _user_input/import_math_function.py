#importing math function
import math
import math as m
x = math.sqrt(25)
print(f"square root of 25 is : {x}")
y = math.sqrt(9)
print(f"square root of 15 is : {y}")
#floor function will always give the lease value
print(f"Floor of 2.9 is {math.floor(2.9)}")
print(f"Floor of 2.6 is {math.floor(2.6)}")
#Ceil function will give you the highest value
print(f"Ceil of 2.8 is {math.ceil(2.8)}")
print(f"Ceil of 2.1 is {math.ceil(2.1)}")
#Power function will return x**n
print(f"The power of 3**4 is {math.pow(3,4)}")
print(f"The power of 3**2 is {math.pow(3,2)}")
#pi value
print(f"pi value : {m.pi}")
#epsilon
print(f"epsilon value : {m.e}")
